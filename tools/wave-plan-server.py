    def png_dims(p: Path) -> tuple[int, int] | None:
        # PNG IHDR: width/height are big-endian uint32 at bytes 16..24.
        with open(p, "rb") as f:
            head = f.read(24)
        if head[:8] != b"\x89PNG\r\n\x1a\n":
            return None
        return (
            int.from_bytes(head[16:20], "big"),
            int.from_bytes(head[20:24], "big"),
        )

