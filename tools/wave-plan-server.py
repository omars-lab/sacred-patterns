        def do_POST(self):
            if self.path == "/api/overrides":
                data = json.loads(self._read_body())
                # Drop empty maps so a fully-undone state deletes cleanly.
                data = {k: v for k, v in data.items() if v}
                ov = plan_dir / "wave-plan-overrides.json"
                if data:
                    ov.write_text(json.dumps(data, indent=2))
                elif ov.exists():
                    ov.unlink()
                try:
                    regen()
                except subprocess.CalledProcessError as e:
                    self.send_error(500, f"plan regeneration failed: {e}")
                    return
                self._send_ok()
            elif self.path == "/api/refresh-pictures":
                # Owner-triggered rebuild of every wave/progress picture from
                # the current renders. WHY this exists: the progress strip and
                # the per-wave thumbnails are baked PNGs (plan-waves.py output),
                # so after the loop ships a new iteration they go stale until a
                # regen runs. This button forces that regen on demand instead of
                # making the owner restart the server.
                self._read_body()
                try:
                    regen()
                    rebuild_progress_strip()
                except subprocess.CalledProcessError as e:
                    self.send_error(500, f"picture refresh failed: {e}")
                    return
                except Exception as e:  # strip rebuild is best-effort
                    self.send_error(500, f"progress strip rebuild failed: {e}")
                    return
                self._send_ok()
            elif self.path == "/api/agree":
                # The wave-plan gate verdict, recorded where the loop reads it
                # (session.json) — no chat transcription step.
                self._read_body()
                s = session()
                wp = s["stage_gates"]["structure"]["wave_plan"]
                wp["agreed"] = True
                wp["agreed_date"] = date.today().isoformat()
                save_session(s)
                self._send_ok()
            elif self.path == "/api/wave-view-pick":
                # The owner's per-wave view pick, recorded where the loop reads
                # it (session.json -> stage_gates.structure.wave_view_choices,
                # a {wave_str: view_key} map). view ∈ {"ref","ours","iso"}; the
                # stable option id the owner sees is f"w{wave}.{view}". This is
                # NOT a stage-gate approval — just which picture to show per ring.
                data = json.loads(self._read_body())
                view = data.get("view", "")
                wave = str(data.get("wave", "")).strip()
                if view not in ("ref", "ours", "iso"):
                    self.send_error(400, "view must be 'ref', 'ours', or 'iso'")
                    return
                if not wave.isdigit():
                    self.send_error(400, "wave must be a wave number")
                    return
                s = session()
                st = s["stage_gates"]["structure"]
                choices = st.setdefault("wave_view_choices", {})
                choices[wave] = view
                st["wave_view_choices_date"] = date.today().isoformat()
                save_session(s)
                self._send_ok()
            elif self.path == "/api/palette-agree":
                self._read_body()
                s = session()
                c = s["stage_gates"]["color"]
                c["palette_agreed"] = True
                c["approved_date"] = date.today().isoformat()
                save_session(s)
                self._send_ok()
            elif self.path == "/api/palette-note":
                note = self._read_body().decode()
                if note.strip():
                    note_path.write_text(note)
                elif note_path.exists():
                    note_path.unlink()
                self._send_ok()
            elif self.path == "/api/wave-verdict":
                # The owner's per-wave call, recorded where the loop reads it
                # (session.json waves_passed[n].owner_verdict) — no chat
                # transcription. state ∈ {"approved","denied",""}; an empty
                # state with a note is a comment-only save. When EVERY built
                # wave is approved, the structure stage auto-flips to approved
                # (the owner asked for per-wave only — the stage gate is a
                # derived rollup, not a separate button).
                data = json.loads(self._read_body())
                wave = str(int(data["wave"]))
                state = data.get("state", "")
                note = (data.get("note") or "").strip()
                s = session()
                wp = s["stage_gates"]["structure"].get("waves_passed", {})
                if wave not in wp:
                    self.send_error(404, f"wave {wave} not built")
                    return
                wp[wave]["owner_verdict"] = {
                    "state": state,
                    "note": note,
                    "date": date.today().isoformat(),
                }
                # Derived stage rollup: approved only when every built wave is
                # approved; a single deny (or any un-judged wave) leaves it open.
                struct = s["stage_gates"]["structure"]
                states = [(v.get("owner_verdict") or {}).get("state") for v in wp.values()]
                if states and all(st == "approved" for st in states):
                    struct["approved"] = True
                    struct["approved_at_iter"] = max(
                        (v.get("iter") for v in wp.values() if isinstance(v.get("iter"), int)),
                        default=None,
                    )
                    struct["approved_date"] = date.today().isoformat()
                else:
                    struct["approved"] = None
                    struct["approved_at_iter"] = None
                save_session(s)
                self._send_ok()
            elif self.path == "/api/merge-verdict":
                # The owner's per-pair "same shape?" call from /simplify. Persisted to
                # merge-verdicts.json in the iteration dir — NOT session.json, and it
                # NEVER touches the structure gate. Keyed "a|b" so a re-judgement just
                # overwrites. state ∈ {"same","different","skip"} (skip not stored).
                data = json.loads(self._read_body())
                a, b = str(data["a"]), str(data["b"])
                state = data.get("state", "")
                mp = merge_verdicts_path()
                if mp is None:
                    self.send_error(404, "no iteration to record against")
                    return
                v = load_merge_verdicts()
                key = f"{a}|{b}"
                if state == "skip":
                    v.pop(key, None)            # an explicit skip clears any prior call
                else:
                    v[key] = {"state": state, "date": date.today().isoformat()}
                mp.write_text(json.dumps(v, indent=1))
                self._send_ok()
            else:
                self.send_error(404)

