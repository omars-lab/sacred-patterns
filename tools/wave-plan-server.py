#!/usr/bin/env python3
"""Serve the session's review experiences on localhost — one front door.

Plain English: this is the CLI the owner runs to review a reconstruction
session. The landing page is a HUB that reads session.json and says, in
words, what needs the owner's eyes today (the wave plan? the colours?),
with one link per item. From there:

- /plan — the wave-plan STUDIO (wave-plan.html, generated by plan-waves.py):
  click any shape, see its wave + flower, fix groupings; "Apply my fixes"
  POSTs here, we persist wave-plan-overrides.json and re-run plan-waves.py.
  The page's "The plan looks right" button records the gate verdict in
  session.json — no chat transcription.
- /palette — the colour gate: the swatch sheet beside the reference;
  "These colours are right" records palette agreement; a note box captures
  corrections (auto-saved, with a visible "saved" confirmation).

Usage:
    /Users/omareid/Workspace/git/qiyas/.venv/bin/python tools/wave-plan-server.py \
        /Users/omareid/Dropbox/Data/sacred-patterns/bikar-medallion-10 \
        --center 386 361 --diameter 738 [--port 8765]

Then open the printed URL. Ctrl-C stops it.

Why a server and not a static page (owner, 2026-06-11): "this should not be
static html, but rather cli commands that runs localhost" — fixes must
persist and re-flow through the plan without the owner shuttling JSON files.
Why a hub (owner, 2026-06-11, review-experience audit —
docs/review-experience-audit.md): four review surfaces with four launch
mechanisms meant nobody could ask "what's waiting for my review?"; the
session.json knows, so the front door reads it back in plain language.
"""

