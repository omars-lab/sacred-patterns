def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("session_dir", type=Path)
    ap.add_argument(
        "--center",
        nargs=2,
        type=float,
        default=None,
        help="override the planner's auto-detected medallion center",
    )
    ap.add_argument(
        "--diameter",
        type=float,
        default=None,
        help="override the planner's auto-detected medallion diameter",
    )
    ap.add_argument("--port", type=int, default=8765)
    args = ap.parse_args()

    session_dir = args.session_dir.resolve()
    analysis_dir = session_dir / "input" / "reference-analysis"
    plan_dir = (analysis_dir / "wave-plan").resolve()
    session_json = session_dir / "session.json"
    note_path = analysis_dir / "palette-feedback.txt"
    tools_dir = Path(__file__).resolve().parent

    def regen() -> None:
        # Re-run the planner with the SAME center/diameter (or let it
        # auto-detect, same as the first run); it auto-loads
        # wave-plan-overrides.json from plan_dir, so owner fixes flow through
        # grouping, flower seating, validation, and every picture.
        cmd = [sys.executable, str(tools_dir / "plan-waves.py"), str(session_dir)]
        if args.center is not None:
            cmd += ["--center", str(args.center[0]), str(args.center[1])]
        if args.diameter is not None:
            cmd += ["--diameter", str(args.diameter)]
        subprocess.run(cmd, check=True)

    def session() -> dict:
        return json.loads(session_json.read_text())

    def save_session(s: dict) -> None:
        session_json.write_text(json.dumps(s, indent=2) + "\n")

    def hub_html() -> str:
        # The front door: read session.json's stage gates and say, in plain
        # words, what needs the owner's eyes — one card per gate, done gates
        # dimmed. No jargon, no Q-numbers, no file paths in the headline.
        s = session()
        gates = s.get("stage_gates", {})
        wp = gates.get("structure", {}).get("wave_plan", {})
        plan_agreed = bool(wp.get("agreed"))
        palette_agreed = bool(gates.get("color", {}).get("palette_agreed"))
        adjudications = wp.get("open_adjudications", "")

        todo = []
        if not plan_agreed:
            extra = ""
            if adjudications:
                extra = (
                    "<p class='muted'>Two groupings we weren't sure about — "
                    "please check them on the plan page (click a shape in the "
                    "group, then move its whole group if we got it wrong):</p>"
                    f"<ul class='muted'><li>{html.escape(adjudications)}</li></ul>"
                )
            todo.append(
                "<div class='gate'><h2>1 — The building plan"
                "<span class='badge open'>needs you</span></h2>"
                "<p>We broke your picture into a few flowers, copied in waves. "
                "Look it over, fix any shape that's in the wrong group, and "
                "press “The plan looks right” when you agree — we "
                "don't start building until then.</p>"
                f"{extra}"
                "<a class='go' href='/plan'>Open the plan</a></div>"
            )
        else:
            todo.append(
                "<div class='gate done'><h2>1 — The building plan"
                "<span class='badge done'>agreed ✓</span></h2>"
                "<p>You agreed to the plan — building is underway. You can "
                "still <a href='/plan'>look at it</a>.</p></div>"
            )
        if (analysis_dir / "swatch-sheet.png").exists():
            if not palette_agreed:
                todo.append(
                    "<div class='gate'><h2>2 — The colours"
                    "<span class='badge open'>needs you</span></h2>"
                    "<p>These are the exact colours we'll paint with. One "
                    "look — are they right?</p>"
                    "<a class='go' href='/palette'>Check the colours</a></div>"
                )
            else:
                todo.append(
                    "<div class='gate done'><h2>2 — The colours"
                    "<span class='badge done'>agreed ✓</span></h2>"
                    "<p>Colours agreed. You can still "
                    "<a href='/palette'>see them</a>.</p></div>"
                )
        n_open = sum("needs you" in t for t in todo)
        head = (
            f"{n_open} thing{'s' if n_open != 1 else ''} need your eyes today."
            if n_open
            else "Nothing waiting on you right now — everything you've agreed to is underway."
        )
        return (
            "<!DOCTYPE html><html lang='en'><head><meta charset='utf-8'>"
            f"<title>Your review list</title><style>{HUB_CSS}</style></head><body>"
            f"<header><h1>Your review list</h1><p>{head}</p>"
            f"<p class='muted'>Pattern: {html.escape(s.get('name', ''))}</p></header>"
            f"<main>{''.join(todo)}</main></body></html>"
        )

    class Handler(SimpleHTTPRequestHandler):
        def __init__(self, *a, **k):
            super().__init__(*a, directory=str(plan_dir), **k)

        # Pictures change on every Apply; the browser must never show a stale
        # wave/flower PNG after a regeneration.
        def end_headers(self):
            self.send_header("Cache-Control", "no-store")
            super().end_headers()

        def _send_html(self, body: str) -> None:
            data = body.encode()
            self.send_response(200)
            self.send_header("Content-Type", "text/html; charset=utf-8")
            self.send_header("Content-Length", str(len(data)))
            self.end_headers()
            self.wfile.write(data)

        def _send_ok(self) -> None:
            body = b'{"ok": true}'
            self.send_response(200)
            self.send_header("Content-Type", "application/json")
            self.send_header("Content-Length", str(len(body)))
            self.end_headers()
            self.wfile.write(body)

        def do_GET(self):
            if self.path in ("/", "/index.html"):
                self._send_html(hub_html())
                return
            if self.path == "/plan":
                self.path = "/wave-plan.html"
            elif self.path == "/palette":
                gates = session().get("stage_gates", {})
                agreed = bool(gates.get("color", {}).get("palette_agreed"))
                note = note_path.read_text() if note_path.exists() else ""
                self._send_html(
                    PALETTE_HTML.replace("__CSS__", HUB_CSS)
                    .replace("__AGREED__", json.dumps(agreed))
                    .replace("__NOTE__", html.escape(note))
                )
                return
            elif self.path == "/swatch-sheet.png":
                # The swatch sheet lives one level above the served plan dir.
                data = (analysis_dir / "swatch-sheet.png").read_bytes()
                self.send_response(200)
                self.send_header("Content-Type", "image/png")
                self.send_header("Content-Length", str(len(data)))
                self.end_headers()
                self.wfile.write(data)
                return
            super().do_GET()

        def _read_body(self) -> bytes:
            n = int(self.headers.get("Content-Length", 0))
            return self.rfile.read(n)

        def do_POST(self):
            if self.path == "/api/overrides":
                data = json.loads(self._read_body())
                # Drop empty maps so a fully-undone state deletes cleanly.
                data = {k: v for k, v in data.items() if v}
                ov = plan_dir / "wave-plan-overrides.json"
                if data:
                    ov.write_text(json.dumps(data, indent=2))
                elif ov.exists():
                    ov.unlink()
                try:
                    regen()
                except subprocess.CalledProcessError as e:
                    self.send_error(500, f"plan regeneration failed: {e}")
                    return
                self._send_ok()
            elif self.path == "/api/agree":
                # The wave-plan gate verdict, recorded where the loop reads it
                # (session.json) — no chat transcription step.
                self._read_body()
                s = session()
                wp = s["stage_gates"]["structure"]["wave_plan"]
                wp["agreed"] = True
                wp["agreed_date"] = date.today().isoformat()
                save_session(s)
                self._send_ok()
            elif self.path == "/api/palette-agree":
                self._read_body()
                s = session()
                c = s["stage_gates"]["color"]
                c["palette_agreed"] = True
                c["approved_date"] = date.today().isoformat()
                save_session(s)
                self._send_ok()
            elif self.path == "/api/palette-note":
                note = self._read_body().decode()
                if note.strip():
                    note_path.write_text(note)
                elif note_path.exists():
                    note_path.unlink()
                self._send_ok()
            else:
                self.send_error(404)

        def log_message(self, fmt, *a):  # quiet: one line per request is noise
            pass

    if not (plan_dir / "wave-plan.html").exists():
        print("no wave-plan.html yet — generating the plan first…")
        regen()

    print(f"Review hub: http://127.0.0.1:{args.port}/")
    print("It lists what needs your eyes; each item links to its page. Ctrl-C to stop.")
    HTTPServer(("127.0.0.1", args.port), Handler).serve_forever()


if __name__ == "__main__":
    main()
